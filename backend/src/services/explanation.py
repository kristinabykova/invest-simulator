def all_explanations(result: dict) -> dict:

    vol = result["volatility"]
    trend = result["trend"]
    roi = result["roi"]
    risk = result["risk"]

    return {
        "explanation": get_explanation(trend),
        "tip": get_tip(trend),
        "term": get_term(trend),
    }


def get_roi_risk_label(roi_percent: float, risk: str) -> str:
    is_profit = roi_percent >= 0
    is_high_roi = roi_percent >= 5

    if is_profit:
        if is_high_roi:
            if risk == "высокий риск":
                return "высокий доход и высокий риск"
            if risk == "умеренный риск":
                return "высокий доход и умеренный риск"
            return "высокий доход и низкий риск"

        else:
            if risk == "высокий риск":
                return "низкий доход и высокий риск"
            if risk == "умеренный риск":
                return "низкий доход и умеренный риск"
            return "низкий доход и низкий риск"

    else:
        if risk == "высокий риск":
            return "убыток и высокий риск"
        if risk == "умеренный риск":
            return "убыток и умеренный риск"
        return "убыток и низкий риск"


def get_explanation(trend) -> str | None:

    if not trend in EXPLAIN:
        return None

    return EXPLAIN[trend]


def get_tip(trend) -> str:

    if trend == "нисходящий тренд":
        return """Не спеши покупать по низкой цене, она может стать еще ниже."""


def get_term(trend) -> str:

    if trend == "нисходящий тренд":
        return """Фиксировать прибыль –  ситуация, когда трейдеры продают акции, 
        которые раньше купили дешевле, чтобы забрать заработанные деньги. 
        Если так делают многие, цена перестает расти и начинает падать. 
        Поэтому фиксация прибыли часто становится началом нисходящего тренда."""

ALLOWED_RISKS = {"низкий риск", "умеренный риск", "высокий риск"}


def normalize_risk(risk: str | None) -> str:
    if risk in ALLOWED_RISKS:
        return risk

    return "умеренный риск"


def get_roi_risk_label(roi_percent: float, risk: str | None) -> str:
    risk = normalize_risk(risk)

    if roi_percent >= 5:
        income_level = "высокий доход"
    elif roi_percent >= 0:
        income_level = "низкий доход"
    else:
        income_level = "убыток"

    return f"{income_level} и {risk}"


def get_volatility_label(volatility: float | None) -> str | None:
    if volatility is None:
        return None

    if volatility >= 1.5:
        return "высокая волатильность"

    if volatility <= 0.5:
        return "низкая волатильность"

    return None


def get_main_tip(
    roi_risk_label: str | None,
    volatility_label: str | None,
    trend: str | None,
) -> str:

    if roi_risk_label in TIPS:
        return TIPS[roi_risk_label]

    if volatility_label in TIPS:
        return TIPS[volatility_label]

    if trend in TIPS:
        return TIPS[trend]

    return (
        "Недостаточно данных для точной подсказки. "
        "Лучше не спешить с покупкой и сначала оценить движение цены."
    )


def build_terms(
    trend: str | None,
    volatility_label: str | None,
    roi: float | None,
    risk: str | None,
) -> list[dict]:

    terms = []

    if trend in TERMS:
        terms.append(TERMS[trend])

    if volatility_label:
        terms.append(TERMS["волатильность"])

    if roi is not None:
        terms.append(TERMS["доходность"])

    if risk is not None:
        terms.append(TERMS["риск"])

    unique_terms = []
    seen = set()

    for item in terms:
        term_name = item["term"]

        if term_name not in seen:
            unique_terms.append(item)
            seen.add(term_name)

    return unique_terms


def all_explanations(result: dict) -> dict:
    trend = result.get("trend")
    volatility = result.get("volatility")
    roi = result.get("roi")
    risk = result.get("risk")

    explanation_parts = []

    if trend in EXPLAIN:
        explanation_parts.append(f"Что произошло с ценой\n{EXPLAIN[trend]}")

    roi_risk_label = None

    if roi is not None:
        roi_risk_label = get_roi_risk_label(roi, risk)

        if roi_risk_label in EXPLAIN:
            explanation_parts.append(
                f"Стоит ли покупать в конце периода\n{EXPLAIN[roi_risk_label]}"
            )

    volatility_label = get_volatility_label(volatility)

    if volatility_label == "высокая волатильность":
        explanation_parts.append(f"Дополнительно:\n{EXPLAIN[volatility_label]}")

    tip = get_main_tip(
        roi_risk_label=roi_risk_label,
        volatility_label=volatility_label,
        trend=trend,
    )

    terms = build_terms(
        trend=trend,
        volatility_label=volatility_label,
        roi=roi,
        risk=risk,
    )

    return {
        "explanation": "\n\n".join(explanation_parts)
        or "Недостаточно данных для объяснения результата.",
        "tip": tip,
        "terms": terms,
    }
